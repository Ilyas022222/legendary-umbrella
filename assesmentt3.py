grade = int(input("please enter your grade: "))
if 70 <= grade <= 100:
    print("A")
elif 60 <= grade <= 69:
    print("B")
elif 50 <= grade <= 59:
    print("C")
elif 40 <= grade <= 49:
    print("D")
elif 30 <= grade <= 39:
    print("E")
elif 20 <= grade <= 29:
    print("F")
    
    
                
            
